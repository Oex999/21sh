#include "../include/shell.h"

int		ft_ispipe(char **args)
{
	int	i;

	i = 0;
	while (args[i] != NULL)
	{
		if (ft_strcmp(args[i], "|") == 0)
			return (i);
		i++;
	}
	return (0);
}
